begin
  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'chats' and policyname = 'chat members can read chats'
  ) then
    create policy "chat members can read chats"
      on public.chats for select
      using (public.is_chat_member(id, auth.uid()));
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'chat_members' and policyname = 'chat members can read memberships'
  ) then
    create policy "chat members can read memberships"
      on public.chat_members for select
      using (public.is_chat_member(chat_id, auth.uid()));
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'chat_messages' and policyname = 'chat members can read messages'
  ) then
    create policy "chat members can read messages"
      on public.chat_messages for select
      using (public.is_chat_member(chat_id, auth.uid()));
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'chat_message_mentions' and policyname = 'chat members can read mentions'
  ) then
    create policy "chat members can read mentions"
      on public.chat_message_mentions for select
      using (
        exists (
          select 1
          from public.chat_messages cm
          where cm.id = message_id
            and public.is_chat_member(cm.chat_id, auth.uid())
        )
      );
  end if;
end $$;
